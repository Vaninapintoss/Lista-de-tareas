    private ActionListener timeAction = new ActionListener()
    {
        @Override
        public void actionPerformed(ActionEvent e) {
            sec++;
            
            if(sec == 60)
            {
                sec = 0;
                min++;
            }
            
            if(min == 60)
            {
                min = 0;
                hour++;
            }
            
            if(hour == 24)
            {
                hour = 0;
            }
            
            updateTimer();
        }      
    };
    
    private void updateTimer()
    {
        String text = (hour<=9?"0":"")+hour+":"+(min<=9?"0":"")+min+":"+(sec<=9?"0":"")+sec;
        text_timer.setText(text);
    }
    
